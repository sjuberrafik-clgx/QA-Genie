/**
 * ═══════════════════════════════════════════════════════════════════════════════
 * INFOGRAPHIC GENERATOR — Poster-Style Infographic Images (PNG)
 * ═══════════════════════════════════════════════════════════════════════════════
 *
 * Generates high-resolution poster infographic images using headless Chromium.
 * Pre-built templates: executive-summary, data-story, comparison,
 *                      process-flow, timeline
 *
 * Each template is a full-page HTML layout rendered to PNG at 2× DPI.
 *
 * @module scripts/infographic-generator
 * ═══════════════════════════════════════════════════════════════════════════════
 */

const fs = require('fs');
const path = require('path');
const { resolveTheme, getOutputDir, generateFileName, hexToRGB } = require('./doc-design-system');

let _browser = null;

async function getBrowser() {
    if (_browser && _browser.isConnected()) return _browser;
    const { chromium } = require('playwright');
    _browser = await chromium.launch({ headless: true });
    return _browser;
}

async function cleanupBrowser() {
    if (_browser) { await _browser.close().catch(() => { }); _browser = null; }
}

// ─── Template Builders ──────────────────────────────────────────────────────

function buildExecutiveSummary(data, theme) {
    const title = data.title || 'Executive Summary';
    const subtitle = data.subtitle || '';
    const metrics = data.metrics || [];
    const highlights = data.highlights || [];
    const conclusion = data.conclusion || '';

    const metricsHtml = metrics.map(m => {
        const statusColor = { good: theme.success, warning: theme.warning, critical: theme.danger }[m.status] || theme.primary;
        return `<div style="text-align:center;padding:20px;background:${theme.surface};border-radius:12px;border-top:4px solid ${statusColor}">
            <div style="font-size:36px;font-weight:800;color:${theme.primary}">${esc(m.value)}</div>
            <div style="font-size:13px;color:${theme.textSecondary};margin-top:4px">${esc(m.label)}</div>
            ${m.change ? `<div style="font-size:12px;color:${statusColor};margin-top:4px">${esc(m.change)}</div>` : ''}
        </div>`;
    }).join('');

    const highlightsHtml = highlights.map((h, i) => `<div style="display:flex;align-items:flex-start;gap:12px;margin-bottom:12px">
        <div style="min-width:32px;height:32px;border-radius:50%;background:${theme.primary};color:#fff;display:flex;align-items:center;justify-content:center;font-weight:700;font-size:14px">${i + 1}</div>
        <div style="font-size:14px;color:${theme.text}">${esc(typeof h === 'string' ? h : h.text || '')}</div>
    </div>`).join('');

    return `<div style="width:1920px;padding:80px;font-family:'Inter',system-ui,sans-serif;background:${theme.background};color:${theme.text}">
        <div style="height:6px;width:100%;background:linear-gradient(90deg,${theme.primary},${theme.accent || theme.primary});border-radius:3px;margin-bottom:40px"></div>
        <h1 style="font-size:48px;font-weight:800;color:${theme.primary};margin-bottom:8px">${esc(title)}</h1>
        ${subtitle ? `<p style="font-size:20px;color:${theme.textSecondary};margin-bottom:40px">${esc(subtitle)}</p>` : '<div style="height:40px"></div>'}
        <div style="display:grid;grid-template-columns:repeat(${Math.min(metrics.length, 4)},1fr);gap:20px;margin-bottom:40px">${metricsHtml}</div>
        ${highlights.length ? `<h2 style="font-size:24px;font-weight:700;color:${theme.primary};margin-bottom:16px">Key Highlights</h2><div style="margin-bottom:30px">${highlightsHtml}</div>` : ''}
        ${conclusion ? `<div style="padding:20px;background:${theme.surface};border-left:4px solid ${theme.primary};border-radius:0 8px 8px 0;font-size:15px;color:${theme.text}">${esc(conclusion)}</div>` : ''}
        <div style="margin-top:40px;text-align:center;font-size:12px;color:${theme.textSecondary}">Generated by DocGenie — ${new Date().toLocaleDateString()}</div>
    </div>`;
}

function buildDataStory(data, theme) {
    const title = data.title || 'Data Story';
    const sections = data.sections || [];

    const sectionsHtml = sections.map((s, i) => {
        const sectionTitle = s.title || `Section ${i + 1}`;
        const value = s.value || '';
        const desc = s.description || s.text || '';
        const icon = s.icon || '';
        return `<div style="background:${theme.surface};border-radius:12px;padding:24px;display:flex;gap:20px;align-items:flex-start">
            ${icon ? `<div style="font-size:36px">${icon}</div>` : ''}
            <div style="flex:1">
                <div style="font-size:16px;font-weight:700;color:${theme.primary}">${esc(sectionTitle)}</div>
                ${value ? `<div style="font-size:32px;font-weight:800;color:${theme.text};margin:8px 0">${esc(value)}</div>` : ''}
                <div style="font-size:14px;color:${theme.textSecondary}">${esc(desc)}</div>
            </div>
        </div>`;
    }).join('');

    return `<div style="width:1920px;padding:80px;font-family:'Inter',system-ui,sans-serif;background:${theme.background};color:${theme.text}">
        <div style="height:6px;width:120px;background:${theme.primary};border-radius:3px;margin-bottom:24px"></div>
        <h1 style="font-size:44px;font-weight:800;color:${theme.primary};margin-bottom:40px">${esc(title)}</h1>
        <div style="display:grid;grid-template-columns:repeat(2,1fr);gap:20px">${sectionsHtml}</div>
        <div style="margin-top:40px;text-align:center;font-size:12px;color:${theme.textSecondary}">Generated by DocGenie</div>
    </div>`;
}

function buildComparison(data, theme) {
    const title = data.title || 'Comparison';
    const items = data.items || [];
    const leftLabel = data.leftLabel || 'Before';
    const rightLabel = data.rightLabel || 'After';

    const rowsHtml = items.map(item => {
        const label = item.label || '';
        const left = item.left || item.before || '';
        const right = item.right || item.after || '';
        return `<tr>
            <td style="padding:12px 16px;font-weight:600;color:${theme.primary};border-bottom:1px solid ${theme.border}">${esc(label)}</td>
            <td style="padding:12px 16px;text-align:center;border-bottom:1px solid ${theme.border}">${esc(left)}</td>
            <td style="padding:12px 16px;text-align:center;border-bottom:1px solid ${theme.border}">${esc(right)}</td>
        </tr>`;
    }).join('');

    return `<div style="width:1920px;padding:80px;font-family:'Inter',system-ui,sans-serif;background:${theme.background};color:${theme.text}">
        <h1 style="font-size:44px;font-weight:800;color:${theme.primary};text-align:center;margin-bottom:40px">${esc(title)}</h1>
        <table style="width:100%;border-collapse:collapse;background:${theme.surface};border-radius:12px;overflow:hidden">
            <thead><tr style="background:${theme.primary};color:${theme.textOnPrimary}">
                <th style="padding:16px;text-align:left;font-size:16px">Category</th>
                <th style="padding:16px;text-align:center;font-size:16px">${esc(leftLabel)}</th>
                <th style="padding:16px;text-align:center;font-size:16px">${esc(rightLabel)}</th>
            </tr></thead>
            <tbody>${rowsHtml}</tbody>
        </table>
        <div style="margin-top:40px;text-align:center;font-size:12px;color:${theme.textSecondary}">Generated by DocGenie</div>
    </div>`;
}

function buildProcessFlow(data, theme) {
    const title = data.title || 'Process Flow';
    const steps = data.steps || [];

    const stepsHtml = steps.map((step, i) => {
        const isLast = i === steps.length - 1;
        return `<div style="display:flex;align-items:flex-start;gap:20px;margin-bottom:${isLast ? 0 : 30}px">
            <div style="display:flex;flex-direction:column;align-items:center">
                <div style="width:48px;height:48px;border-radius:50%;background:${theme.primary};color:#fff;display:flex;align-items:center;justify-content:center;font-weight:800;font-size:18px">${i + 1}</div>
                ${!isLast ? `<div style="width:3px;height:30px;background:${theme.border};margin-top:4px"></div>` : ''}
            </div>
            <div style="flex:1;padding-top:4px">
                <div style="font-size:18px;font-weight:700;color:${theme.primary}">${esc(step.title || step.name || `Step ${i + 1}`)}</div>
                <div style="font-size:14px;color:${theme.textSecondary};margin-top:4px">${esc(step.description || step.text || '')}</div>
            </div>
        </div>`;
    }).join('');

    return `<div style="width:1920px;padding:80px;font-family:'Inter',system-ui,sans-serif;background:${theme.background};color:${theme.text}">
        <div style="height:6px;width:120px;background:${theme.primary};border-radius:3px;margin-bottom:24px"></div>
        <h1 style="font-size:44px;font-weight:800;color:${theme.primary};margin-bottom:40px">${esc(title)}</h1>
        <div style="max-width:800px">${stepsHtml}</div>
        <div style="margin-top:40px;text-align:center;font-size:12px;color:${theme.textSecondary}">Generated by DocGenie</div>
    </div>`;
}

function buildTimeline(data, theme) {
    const title = data.title || 'Timeline';
    const events = data.events || data.milestones || [];

    const eventsHtml = events.map((ev, i) => {
        const isLeft = i % 2 === 0;
        const date = ev.date || ev.time || '';
        const label = ev.title || ev.label || '';
        const desc = ev.description || ev.text || '';
        return `<div style="display:flex;justify-content:${isLeft ? 'flex-end' : 'flex-start'};width:50%;${isLeft ? 'padding-right:40px' : 'margin-left:50%;padding-left:40px'};margin-bottom:30px;position:relative">
            <div style="position:absolute;${isLeft ? 'right:-10px' : 'left:-10px'};top:10px;width:20px;height:20px;border-radius:50%;background:${theme.primary};border:3px solid ${theme.background}"></div>
            <div style="background:${theme.surface};border-radius:12px;padding:20px;max-width:380px;width:100%">
                ${date ? `<div style="font-size:12px;color:${theme.textSecondary};font-weight:600;margin-bottom:4px">${esc(date)}</div>` : ''}
                <div style="font-size:16px;font-weight:700;color:${theme.primary}">${esc(label)}</div>
                <div style="font-size:14px;color:${theme.text};margin-top:4px">${esc(desc)}</div>
            </div>
        </div>`;
    }).join('');

    return `<div style="width:1920px;padding:80px;font-family:'Inter',system-ui,sans-serif;background:${theme.background};color:${theme.text}">
        <h1 style="font-size:44px;font-weight:800;color:${theme.primary};text-align:center;margin-bottom:50px">${esc(title)}</h1>
        <div style="position:relative">
            <div style="position:absolute;left:50%;top:0;bottom:0;width:3px;background:${theme.border};transform:translateX(-50%)"></div>
            ${eventsHtml}
        </div>
        <div style="margin-top:40px;text-align:center;font-size:12px;color:${theme.textSecondary}">Generated by DocGenie</div>
    </div>`;
}

function esc(text) {
    return String(text || '').replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

const TEMPLATE_BUILDERS = {
    'executive-summary': buildExecutiveSummary,
    'data-story': buildDataStory,
    comparison: buildComparison,
    'process-flow': buildProcessFlow,
    timeline: buildTimeline,
};

// ─── Main Generator ─────────────────────────────────────────────────────────

/**
 * Generate a poster-style infographic image (PNG).
 *
 * @param {Object} options
 * @param {string} options.template - Template name (executive-summary, data-story, comparison, process-flow, timeline)
 * @param {Object} options.data - Template-specific data
 * @param {string|Object} [options.theme] - Theme name or override
 * @param {number} [options.width] - Output width in CSS pixels (default: 1920)
 * @param {number} [options.height] - Output height (auto-fit if omitted)
 * @param {string} [options.outputPath] - Custom output path
 * @returns {Promise<Object>} { success, filePath, fileName, width, height, fileSize }
 */
async function generateInfographic(options) {
    const { template = 'executive-summary', data = {}, theme: themeInput, width = 1920, height, outputPath } = options;

    const builder = TEMPLATE_BUILDERS[template];
    if (!builder) {
        return { success: false, error: `Unknown template: ${template}. Available: ${Object.keys(TEMPLATE_BUILDERS).join(', ')}` };
    }

    const theme = resolveTheme(themeInput);
    const html = `<!DOCTYPE html><html><head><meta charset="UTF-8">
<link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;600;700;800&display=swap" rel="stylesheet">
</head><body style="margin:0;padding:0">${builder(data, theme)}</body></html>`;

    const browser = await getBrowser();
    const context = await browser.newContext({ deviceScaleFactor: 2 });
    const page = await context.newPage();

    try {
        await page.setViewportSize({ width, height: height || 1080 });
        await page.setContent(html, { waitUntil: 'networkidle' });

        // Auto-height: measure content
        const bodyHeight = height || await page.evaluate(() => document.body.scrollHeight);
        await page.setViewportSize({ width, height: bodyHeight });

        const fileName = generateFileName(data.title || template, '.png');
        const outDir = getOutputDir();
        const filePath = outputPath || path.join(outDir, fileName);
        const dir = path.dirname(filePath);
        if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });

        await page.screenshot({ path: filePath, fullPage: true, type: 'png' });

        const stats = fs.statSync(filePath);

        return {
            success: true,
            filePath,
            fileName: path.basename(filePath),
            width: width * 2,
            height: bodyHeight * 2,
            fileSize: stats.size,
            fileSizeHuman: `${(stats.size / 1024).toFixed(1)} KB`,
            template,
        };
    } finally {
        await context.close();
    }
}

module.exports = { generateInfographic, cleanupBrowser, TEMPLATE_BUILDERS };
